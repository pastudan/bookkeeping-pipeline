import { writeFileSync } from "fs";
import { join } from "path";
import { paths, loadJSON, ENTITY_ROOT, parseYear, entity } from "./config.js";

const YEAR = parseYear();

function formatAmount(amount) {
  return Math.abs(amount).toFixed(2);
}

function padAccount(account, targetCol = 50) {
  const padding = Math.max(1, targetCol - account.length);
  return account + " ".repeat(padding);
}

function generateOpenDirectives(chart) {
  const lines = [];
  const groups = {};

  for (const acct of chart.accounts) {
    const top = acct.account.split(":")[0];
    if (!groups[top]) groups[top] = [];
    groups[top].push(acct);
  }

  const groupOrder = ["Assets", "Liabilities", "Equity", "Income", "Expenses"];

  for (const group of groupOrder) {
    const accounts = groups[group];
    if (!accounts || accounts.length === 0) continue;

    lines.push(`; ${"-".repeat(76)}`);
    lines.push(`; ${group.toUpperCase()}`);
    lines.push(`; ${"-".repeat(76)}`);
    lines.push("");

    for (const acct of accounts) {
      lines.push(`1900-01-01 open ${padAccount(acct.account)}USD`);
      if (acct.description) {
        lines.push(`  description: "${acct.description}"`);
      }
      lines.push("");
    }
  }

  return lines.join("\n");
}

function generateTransaction(txn) {
  const lines = [];
  const mercury = txn.mercury || {};
  const bookkeeping = txn.bookkeeping || {};
  const ocr = txn.ocr || null;

  const date = mercury.postedAt ? mercury.postedAt.split("T")[0] : "Unknown";
  const desc = (mercury.counterpartyName || mercury.note || "Unknown").replace(/"/g, '\\"');
  const amount = parseFloat(mercury.amount || 0);
  const account = bookkeeping.account || "Expenses:Other";

  const tag = txn.id ? ` #mercury-${txn.id}` : "";

  lines.push(`${date} * "${desc}"${tag}`);

  if (bookkeeping.note) {
    lines.push(`  note: "${bookkeeping.note.replace(/"/g, '\\"')}"`);
  }
  if (bookkeeping.reasoning && !bookkeeping.override) {
    lines.push(`  reasoning: "${bookkeeping.reasoning.replace(/"/g, '\\"')}"`);
  }
  if (ocr?.items && ocr.items.length > 0) {
    const itemNames = ocr.items.map((i) => i.name).join(", ");
    lines.push(`  items: "${itemNames.replace(/"/g, '\\"')}"`);
  }
  if (ocr?.summary) {
    lines.push(`  receipt-summary: "${ocr.summary.replace(/"/g, '\\"')}"`);
  }
  if (bookkeeping.confidence) {
    lines.push(`  confidence: "${bookkeeping.confidence}"`);
  }

  // Postings — determine debit/credit based on amount direction
  if (amount < 0) {
    // Outgoing money: debit expense/asset, credit Mercury
    lines.push(`  ${padAccount(account)}${formatAmount(amount)} USD`);
    lines.push(`  ${padAccount("Assets:Bank:Mercury")}-${formatAmount(amount)} USD`);
  } else if (amount > 0) {
    // Incoming money: debit Mercury, credit equity/income
    lines.push(`  ${padAccount("Assets:Bank:Mercury")}${formatAmount(amount)} USD`);
    lines.push(`  ${padAccount(account)}-${formatAmount(amount)} USD`);
  }

  return lines.join("\n");
}

function main() {
  const transactions = loadJSON(paths.transactions);
  const chart = loadJSON(paths.chartOfAccounts);

  // Only compile transactions that have been categorized and belong to YEAR
  const categorized = transactions.filter((t) => {
    if (!t.bookkeeping) return false;
    const postedAt = t.mercury?.postedAt || "";
    return postedAt.startsWith(`${YEAR}-`);
  });

  console.log(`Compiling beancount file for ${YEAR} ...`);
  console.log(`  ${categorized.length} categorized transactions`);
  console.log(`  ${chart.accounts.length} accounts`);

  // Sort transactions by date (newest first, matching existing file convention)
  const sorted = [...categorized].sort((a, b) => {
    const da = a.mercury?.postedAt || "0000-00-00";
    const db = b.mercury?.postedAt || "0000-00-00";
    return db.localeCompare(da);
  });

  const sections = [];

  sections.push(`; ${YEAR} Transactions - ${entity.name}`);
  sections.push(`; Generated by pipeline on ${new Date().toISOString().split("T")[0]}`);
  sections.push(`; ${sorted.length} transactions from Mercury`);
  sections.push("");

  sections.push("; " + "=".repeat(76));
  sections.push("; IMPORTED TRANSACTIONS");
  sections.push("; " + "=".repeat(76));
  sections.push("");

  for (const txn of sorted) {
    sections.push(generateTransaction(txn));
    sections.push("");
  }

  sections.push("; " + "=".repeat(76));
  sections.push("; MANUAL TRANSACTIONS");
  sections.push("; " + "=".repeat(76));
  sections.push("");
  sections.push("; Add manual transactions below (cash payments, accruals, depreciation, etc.)");
  sections.push("");

  const beancountContent = sections.join("\n");

  const outputPath = join(ENTITY_ROOT, `${YEAR}.beancount`);
  writeFileSync(outputPath, beancountContent);
  console.log(`Wrote ${outputPath}`);

  // Summary
  const accountCounts = {};
  for (const txn of sorted) {
    const acct = txn.bookkeeping?.account || "Expenses:Other";
    accountCounts[acct] = (accountCounts[acct] || 0) + 1;
  }

  console.log(`\nAccount breakdown:`);
  const acctSorted = Object.entries(accountCounts).sort((a, b) => b[1] - a[1]);
  for (const [acct, count] of acctSorted) {
    console.log(`  ${acct}: ${count}`);
  }
}


main();
